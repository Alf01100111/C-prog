void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            // image[i,j] = new Random().Next(1,10);
            if (image[i, j] == 0) System.Console.Write($" ");
            else System.Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int cow)
{
    if (pic[row, cow] == 0)
    {
        pic[row, cow] = 1;
        FillImage(row - 1, cow);
        FillImage(row, cow - 1);
        FillImage(row + 1, cow);
        FillImage(row, cow + 1);
    }
}
